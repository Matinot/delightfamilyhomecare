	<script>
      $(function(){

        //in this case we are using AJAX to submit this contact form and to get the response in javascript

        var form = $("#contactForm");

        form.submit(function (e){
          $(this).attr("disable", "disable"); //disable submit button to prevent
          e.preventDefault();

          $.ajax({
            type: form.attr("method"), //post
            url: form.attr('action'), // send_message.php
            data: form.serialize(), // values of all fields
            dataType:"json",
            success: function (data) {
              // print JSON response content here retutned from the php file send_message.php
              $(".response").text(data.content);
              //check response value
              if (data.response=="success") {
                $(".response").css("color","green");
              }
              else{
                $(".response").css("color","red");
              }
              // Once the response is returned then the submit button must be reactivated
              $("input:submit").removeAttr("disable");
              $("#contactForm")[0].reset();
            },
            error: function (data) { // error on Ajax Failure
              $(".response").text("An error occurred");
              $(".response").css("color","red");
              $("input:submit").removeAttr("disable");
              $(':input','#contactForm').not(':button, :submit, :reset, :hidden').val('').removeAttr('checked').removeAttr('selected');
              $("#contactForm").trigger("reset");
            }
          }); 
        });   
      });
    </script>
